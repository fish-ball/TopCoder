class AmebaDiv1 {
public:
	int count(vector <int>);
};

int AmebaDiv1::count(vector <int> X) {
	set<int> s;
	int n = X.size();
	for(int i = 0; i < n; ++i) {
		if(s.find(X[i]) == s.end()) {
			s.erase(X[i]*2);
		}
		s.insert(X[i]);
	}
	return s.size();
}


